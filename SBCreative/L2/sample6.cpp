#include <iostream>
using namespace std;

int main()
{
	cout << "10進数の10は" << 10 << "です．\n";
	cout << "8進数の06は" << 06 << "です．\n";
	cout << "16進数の0x6は" << 0x6 << "です．\n";
	cout << "8進数の024は" << 024 << "です．\n";
	cout << "16進数の0x5Fは" << 0x14 << "です．\n";
	cout << "8進数の015は" << 015 << "です．\n";
	cout << "16進数の0xDは" << 0xD << "です．\n";
	return 0;
}
